msg='strangs and stuff'
#    012345678 ect.
print(msg)
#upper/lower will change case of chars, either all upper or all lower.
print(msg.upper())
#capitalize will capitalize first char
print(msg.capitalize())
#title will capitalize beginning letter of every word
print(msg.title())
#len is the length of msg
print(len(msg))
#counts the instance of whatever string/char
print(msg.count('s'))
#slicing can get pretty wild
#-1 will start with last, -3 is third from last, ect.
print(msg[-1])
#starts with 2 position ends with 7, but does not include 7
print(msg[2:7])
